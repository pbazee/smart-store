export async function ensurePopupStorage() {
  await runSchemaRepair("popup", async () => {
    await executeRepairStatements([
      `
        DO $$
        BEGIN
          IF EXISTS (
            SELECT 1
            FROM pg_type
            WHERE typname = 'PopupDisplayTarget'
          ) THEN
            BEGIN
              ALTER TYPE "PopupDisplayTarget" ADD VALUE IF NOT EXISTS 'shop';
            EXCEPTION
              WHEN duplicate_object THEN NULL;
            END;
            BEGIN
              ALTER TYPE "PopupDisplayTarget" ADD VALUE IF NOT EXISTS 'product';
            EXCEPTION
              WHEN duplicate_object THEN NULL;
            END;
          END IF;
        END $$;
      `,
    ]);
  });
}

