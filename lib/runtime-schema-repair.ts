import { shouldUseMockData } from "@/lib/live-data-mode";
import { prisma } from "@/lib/prisma";

const schemaRepairTasks = new Map<string, Promise<void>>();

async function runSchemaRepair(key: string, repair: () => Promise<void>) {
  if (shouldUseMockData()) {
    return;
  }

  let task = schemaRepairTasks.get(key);
  if (!task) {
    task = repair().catch((error) => {
      schemaRepairTasks.delete(key);
      throw error;
    });
    schemaRepairTasks.set(key, task);
  }

  await task;
}

async function executeRepairStatements(statements: string[]) {
  for (const statement of statements) {
    await prisma.$executeRawUnsafe(statement);
  }
}

export async function ensureStoreSettingsStorage() {
  await runSchemaRepair("store-settings", async () => {
    await executeRepairStatements([
      `
        CREATE TABLE IF NOT EXISTS "StoreSettings" (
          "id" INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
          "supportEmail" TEXT,
          "supportPhone" TEXT,
          "adminNotificationEmail" TEXT,
          "contactPhone" TEXT,
          "footerContactPhone" TEXT,
          "createdAt" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
          "updatedAt" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP
        );
      `,
      `ALTER TABLE "StoreSettings" ADD COLUMN IF NOT EXISTS "supportEmail" TEXT;`,
      `ALTER TABLE "StoreSettings" ADD COLUMN IF NOT EXISTS "supportPhone" TEXT;`,
      `ALTER TABLE "StoreSettings" ADD COLUMN IF NOT EXISTS "adminNotificationEmail" TEXT;`,
      `ALTER TABLE "StoreSettings" ADD COLUMN IF NOT EXISTS "contactPhone" TEXT;`,
      `ALTER TABLE "StoreSettings" ADD COLUMN IF NOT EXISTS "footerContactPhone" TEXT;`,
      `ALTER TABLE "StoreSettings" ADD COLUMN IF NOT EXISTS "createdAt" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP;`,
      `ALTER TABLE "StoreSettings" ADD COLUMN IF NOT EXISTS "updatedAt" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP;`,
    ]);
  });
}

