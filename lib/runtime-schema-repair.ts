import { shouldUseMockData } from "@/lib/live-data-mode";
import { prisma } from "@/lib/prisma";

const schemaRepairTasks = new Map<string, Promise<void>>();

async function runSchemaRepair(key: string, repair: () => Promise<void>) {
  if (shouldUseMockData()) {
    return;
  }

  let task = schemaRepairTasks.get(key);
  if (!task) {
    task = repair().catch((error) => {
      schemaRepairTasks.delete(key);
      throw error;
    });
    schemaRepairTasks.set(key, task);
  }

  await task;
}

async function executeRepairStatements(statements: string[]) {
  for (const statement of statements) {
    await prisma.$executeRawUnsafe(statement);
  }
}

